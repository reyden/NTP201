    private class HighlightRenderer extends DefaultTableCellRenderer {
        
    @Override
    public Component getTableCellRendererComponent(JTable table, Object value, 
            boolean isSelected, boolean hasFocus, int row, int column){
        
        // everything as usual
        super.getTableCellRendererComponent(table, value, isSelected, hasFocus, 
                row, column);
        
        // added behavior
        
        String toSearch = string_to_color;
        if(toSearch.isEmpty()){
            table.setModel(jtbl_viewFiles.getModel());
        }
        else
        {  
            if (value!=null && value.toString().toLowerCase().matches(".*"+
                    Pattern.quote(string_to_color.toLowerCase())+
                    ".*")){
                //customize the border
                setBorder(BorderFactory.createMatteBorder(2, 2, 2, 2
                    ,new java.awt.Color(51, 153, 255)));
                //customize the font
                setFont(new java.awt.Font("Tahoma", 1, 14));
        }    
        }
        return this; 
    }
    }
