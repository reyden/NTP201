    private void searchTheString(String str){
        for(int i = 0; i < jtbl_viewFiles.getRowCount(); i++){
            String value = jtbl_viewFiles.getValueAt(i, 2).toString() + jtbl_viewFiles
                    .getValueAt(i, 3).toString();
            if (value!=null && value.toLowerCase().matches(".*"+Pattern
                    .quote(str.toLowerCase())+".*"))
            {
                 jtbl_viewFiles.setRowSelectionInterval(i, i);
                 
                 Rectangle r = jtbl_viewFiles.getCellRect(i, 3, true);
                 jtbl_viewFiles.scrollRectToVisible(r);
                 jtbl_viewFiles.setDefaultRenderer(Object.class,new HighlightRenderer());
                 jtbl_viewFiles.repaint();
                 
            }else{
                jtbl_viewFiles.getModel();
                jtbl_viewFiles.repaint();
            }
        }
    }
    
